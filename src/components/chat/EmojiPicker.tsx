export function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const [search, setSearch] = useState('');
  const [activeCategory, setActiveCategory] = useState(0);

  const filteredCategories = useMemo(() => {
    if (!search) return EMOJI_CATEGORIES;
    
    const suggestions = getEmojiSuggestions(search, 50);
    if (suggestions.length > 0) {
      return [{
        name: 'Search Results',
        emojis: suggestions.map(s => s.emoji)
      }];
    }
    
    // Filter by emoji that might contain the search term in visual representation
    return EMOJI_CATEGORIES.map(cat => ({
      ...cat,
      emojis: cat.emojis.slice(0, 20) // Just show first 20 from each category
    })).filter(cat => cat.emojis.length > 0);
  }, [search]);

  return (
    <div className="absolute bottom-full left-0 mb-2 w-80 bg-card border border-border rounded-xl shadow-2xl overflow-hidden z-50">
      {/* Header */}
      <div className="flex items-center justify-between p-3 border-b border-border">
        <span className="font-semibold text-sm">Emojis</span>
        <button
          onClick={onClose}
          className="p-1 hover:bg-muted rounded-lg transition-colors"
        >
          <X className="w-4 h-4" />
        </button>
      </div>
      
      {/* Search */}
      <div className="p-2 border-b border-border">
        <div className="relative">
          <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-muted-foreground" />
          <input
            type="text"
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            placeholder="Search emojis or type :code:"
            className="w-full bg-muted border border-border/50 rounded-lg pl-9 pr-4 py-2 text-sm focus:outline-none focus:border-primary"
            autoFocus
          />
        </div>
      </div>
      
      {/* Category tabs */}
      {!search && (
        <div className="flex gap-1 p-2 border-b border-border overflow-x-auto scrollbar-hide">
          {EMOJI_CATEGORIES.map((cat, idx) => (
            <button
              key={cat.name}
              onClick={() => setActiveCategory(idx)}
              className={`px-2 py-1 rounded text-xs whitespace-nowrap transition-colors ${
                activeCategory === idx ? 'bg-primary text-primary-foreground' : 'hover:bg-muted'
              }`}
            >
              {cat.name}
            </button>
          ))}
        </div>
      )}
      
      {/* Emoji grid */}
      <div className="h-48 overflow-y-auto p-2">
        {filteredCategories.map((category, catIdx) => (
          <div key={category.name} className={!search && catIdx !== activeCategory ? 'hidden' : ''}>
            {search && (
              <p className="text-xs text-muted-foreground mb-2 px-1">{category.name}</p>
            )}
            <div className="grid grid-cols-8 gap-1">
              {category.emojis.map((emoji, idx) => (
                <button
                  key={`${emoji}-${idx}`}
                  onClick={() => {
                    onSelect(emoji);
                    onClose();
                  }}
                  className="w-8 h-8 flex items-center justify-center text-xl hover:bg-muted rounded transition-colors"
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>
        ))}
      </div>
      
      {/* Footer hint */}
      <div className="p-2 border-t border-border">
        <p className="text-xs text-muted-foreground text-center">
          Type <code className="bg-muted px-1 rounded">:emoji:</code> for shortcuts (e.g. :skull:)
        </p>
      </div>
    </div>
  );
}
